function cExpScore(A, B){
    let expScore;
    //If A is lower
    if (A.rank<B.rank){
        expScore = 1/(1+(Math.E^((B.rank-A.rank)/400)));
    }
    //If B is lower
    else if (B.rank<A.rank){
        expScore = 1-1/(1+(Math.E^((A.rank-B.rank)/400)));
    }
    //If tie
    else {
        expScore = 0.5;
    };
    //console.log(expScore);
    return(expScore);
};

